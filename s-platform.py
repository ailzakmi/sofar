# Возвращает строку, представляющую текущую редакцию Windows. Возможные значения включают, но не ограничиваются: 'Enterprise', 'IoTUAP', 'ServerStandard' и 'nanoserver'.
print(f"Возвращает строку, представляющую текущую редакцию Windows: {platform.win32_edition()}")
# Возвращает True, если версия Windows, возвращенная platform.win32_edition(), распознается как версия IoT.
print(f"Возвращает True, если версия Windows IoT: {platform.win32_is_iot()}")

