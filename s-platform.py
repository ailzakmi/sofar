import platform

def get_like_distro():
    info = platform.freedesktop_os_release()
    ids = [info["ID"]]
    if "ID_LIKE" in info:
        # ids are space separated and ordered by precedence
        ids.extend(info["ID_LIKE"].split())
    return ids

print("--- Кроссплатформенные функции ---")
# Возвращает кортеж (bits, linkage), который содержит информацию об архитектуре битов и формате связи, 
# используемом для исполняемого файла. Оба значения возвращаются в виде строк.
print(f"Архитектура: {platform.architecture()}")
# Возвращает тип машины, например 'I386'.
print(f"Машина: {platform.machine()}")
# Возвращает сетевое имя компьютера, может быть не полностью!
print(f"Сетевое имя: {platform.node()}")
# Возвращает одну строку, идентифицирующую базовую платформу, с максимально возможным количеством полезной информации
print(f"Платформа: {platform.platform()}")
# Возвращает реальное имя процессора, например 'Amdk6'.
print(f"Имя процессора: {platform.processor()}")
# Возвращает кортеж (buildno, builddate) с указанием номера и даты сборки Python в виде строк.
print(f"Номер и дата сборки Python: {platform.python_build()}")
# Возвращает строку, идентифицирующую компилятор, используемый для компиляции Python.
print(f"Идентификатор компилятора: {platform.python_compiler()}")
# Возвращает строку, идентифицирующую ветвь SCM реализации Python.
print(f"Идентификатор ветви SCM: {platform.python_branch()}")
# Возвращает строку, идентифицирующую реализацию Python. 
# Возможные возвращаемые значения: ‘CPython’, ‘IronPython’, ‘Jython’, ‘PyPy’.
print(f"Идентификатор реализации Python: {platform.python_implementation()}")
# Возвращает строку, идентифицирующую ревизию SCM реализации Python.
print(f"Идентификатор ревизии SCM: {platform.python_revision()}")
# Возвращает версию Python в виде строки 'major.minor.patchlevel'.
# Обратите внимание, что в отличие от Python sys.version(), возвращаемое значение всегда будет включать уровень исправления (по умолчанию 0).
print(f"Версия Python в виде строки: {platform.python_version()}")
# Возвращает версию Python как кортеж (major, minor, patchlevel) строк.
print(f"Версия Python как кортеж: {platform.python_version_tuple()}")
# Возвращает выпуск системы, например, '2.2.0' или 'NT'.
print(f"Возвращает выпуск системы: {platform.release()}")
# Возвращает имя операционной системы такое как 'Linux', 'Darwin', 'Java', 'Windows'.
print(f"Возвращает имя операционной системы: {platform.system()}")
# Возвращает (system, release, version), связанные с общими маркетинговыми именами, используемыми в некоторых системах.
print(f"Возвращает (system, release, version): {platform.system_alias('Ubuntu', '18', '4')}")
# Возвращает версию выпуска системы, например '#3 on degas'.
print(f"Версия выпуска системы: {platform.version()}")
# Возвращает именованный кортеж, содержащий шесть атрибутов: system, node, release, version, machine и processor.
print(f"Возвращает именованный кортеж, содержащий шесть атрибутов: {platform.uname()}")

print("--- Функции платформы Windows ---")
# Получает дополнительную информацию о версии из реестра Windows и возвращает кортеж (release, version, csd, ptype), 
# относящийся к выпуску ОС, номеру версии, уровню CSD (пакет обновления) и типу ОС (многопроцессорный/однопроцессорный).
